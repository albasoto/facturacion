    private void btnBuscarPedidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPedidosActionPerformed
        
         try{
           Connection con = ConexionMySQL.connect();
            Statement s =  con.createStatement();            
            ResultSet pedidos = s.executeQuery("select codPedido,fechaPedido,NomProvee,NomProducto,codProducto,numUnidades,precioProd\n" +
            "from pedidosprov as d JOIN proveedores as p on p.CodProvee=d.Cod_provee_FK");
            
            DefaultTableModel modelo =new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"codPedido","fechaPedido","NomProvee","NomProducto","codProducto" ,"numUnidades","precioProd"});
            while(pedidos.next()){
             modelo.addRow(new Object[]{pedidos.getString("codPedido"),pedidos.getString("fechaPedido"),pedidos.getString("NomProvee"),pedidos.getString("NomProducto"),pedidos.getString("codProducto") ,pedidos.getString("numUnidades"),pedidos.getString("precioProd")});
            }
            
            tablaPedidos.setModel(modelo);
            
       }catch (SQLException error) {
           
                System.out.println(error.getMessage());
            }
    }//GEN-LAST:event_btnBuscarPedidosActionPerformed

